-- Script para criação da tabela AS_PARCEIRO_EMPRES_GRUPO_ICMS no Oracle
-- Reconfigurada para usar ID sequencial como PK para maior resiliência

CREATE TABLE AS_PARCEIRO_EMPRES_GRUPO_ICMS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_SISTEMA NUMBER NOT NULL,
    CODEMP NUMBER,
    CODPARC NUMBER,
    CODTAB NUMBER,
    GRUPOICMS NUMBER,
    CLASSIFICMS VARCHAR2(50),
    FORMULA VARCHAR2(4000),
    INDPRECOEMBUT NUMBER,
    RETEMISS VARCHAR2(10),
    SANKHYA_ATUAL CHAR(1) DEFAULT 'S' CHECK (SANKHYA_ATUAL IN ('S', 'N')),
    DT_ULT_CARGA TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    DT_CRIACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Índices para performance e unicidade lógica (sem forçar NOT NULL)
CREATE INDEX IDX_AS_PARC_EMP_GR_ICMS_SYNC ON AS_PARCEIRO_EMPRES_GRUPO_ICMS(ID_SISTEMA, CODEMP, CODPARC, CODTAB, GRUPOICMS);
CREATE INDEX IDX_AS_PARC_EMP_GR_ICMS_SIST ON AS_PARCEIRO_EMPRES_GRUPO_ICMS(ID_SISTEMA);
CREATE INDEX IDX_AS_PARC_EMP_GR_ICMS_ATUAL ON AS_PARCEIRO_EMPRES_GRUPO_ICMS(SANKHYA_ATUAL);

COMMENT ON TABLE AS_PARCEIRO_EMPRES_GRUPO_ICMS IS 'Sincronização de regras de ICMS (ParceiroEmpresGrupoIcms) com ID sequencial';
COMMENT ON COLUMN AS_PARCEIRO_EMPRES_GRUPO_ICMS.ID IS 'ID Único Autoincrementado';
COMMENT ON COLUMN AS_PARCEIRO_EMPRES_GRUPO_ICMS.ID_SISTEMA IS 'ID do sistema/contrato';
COMMENT ON COLUMN AS_PARCEIRO_EMPRES_GRUPO_ICMS.CODEMP IS 'Código da Empresa';
COMMENT ON COLUMN AS_PARCEIRO_EMPRES_GRUPO_ICMS.CODPARC IS 'Código do Parceiro';
COMMENT ON COLUMN AS_PARCEIRO_EMPRES_GRUPO_ICMS.CODTAB IS 'Tabela de Preço';
COMMENT ON COLUMN AS_PARCEIRO_EMPRES_GRUPO_ICMS.GRUPOICMS IS 'Grupo de ICMS';

COMMIT;
